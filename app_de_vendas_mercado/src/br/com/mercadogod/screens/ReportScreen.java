    private void report(){
        String query = "select s.idvenda as IdVenda, s.selldate as DataV, p.productid as IdProd, p.prodname as Produto, p.prodval as Validade, s.prodmount as Quantidade, p.prodprice as Pre√ßo, s.prodlucro as Lucro, p.prodvalue as ValorOrigi,s.prodreal as VendaOrigi from saletabletrue s join prodtable p on s.productid=p.productid order by idvenda;";
        try{
            pst = connection.prepareStatement(query);
            rs = pst.executeQuery();
            SelledTable.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(SQLException e){
            
        }
    }
    private void valorTot(){
        String valRecCli="0";
        String valTot="0";
        String query_valRecCli = "select sum(totprice) from clitable";
        String query_valTot = "select sum(s.prodlucro) as Lucro from saletabletrue s join prodtable p on s.productid=p.productid";
        String query_valRealTot = "select sum(prodlucro)-sum(prodreal) from saletabletrue";
        try{
            pst = connection.prepareStatement(query_valRecCli);
            rs = pst.executeQuery();
            if(rs.next()){
                if(rs.getString(1)==null){
                    txtValRecCli.setText("0.00");
                    valRecCli="0";
                } else {
                    txtValRecCli.setText(rs.getString(1));
                    valRecCli=rs.getString(1);
                }
            }
            
            pst = connection.prepareStatement(query_valTot);
            rs = pst.executeQuery();
            if(rs.next()){
                if(rs.getString(1)==null){
                    txtValTot.setText("0.00");
                    valTot="0";
                } else {
                    txtValTot.setText(rs.getString(1));
                    valTot=rs.getString(1);
                }
            }
            double resultado = Double.parseDouble(valTot)-Double.parseDouble(valRecCli);
            txtValReal.setText(df.format(resultado));
            
            pst = connection.prepareStatement(query_valRealTot);
            rs = pst.executeQuery();
            if(rs.next()){
                if(rs.getString(1)==null){
                    txtLucroReal.setText("0.00");
                } else {
                    double realResult= Double.parseDouble(rs.getString(1))-Double.parseDouble(valRecCli);
                    txtLucroReal.setText(df.format(realResult));
                }
            }
        }catch(SQLException e){
            
        }
    }
    
